package searching;

public class Pike {

    private String pikeName;
    private int pikeHeight;

    public Pike(String pikeName, int pikeHeight) {
        this.pikeName = pikeName;
        this.pikeHeight = pikeHeight;
    }

    public String getPikeName() {
        return pikeName;
    }

    public int getPikeHeight() {
        return pikeHeight;
    }

}
